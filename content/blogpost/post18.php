<h1>Robeats</h1>
<p>Posted by <b>SPOTCO</b> on April 2017</p>
<hr/>

Working on a MMO rhythm game (tentatively called &quot;Robeats&quot;) on Roblox for a few months.<br/>
I wanted to do a rhythm game done right - social, &quot;Roblox-y&quot; and lots of retention/monetization hooks. <br/>
There have been a couple other rhythm games done on Roblox (like <a href="https://www.roblox.com/games/290276263/Rhythm-Master">Rhythm Master</a> 
by Xsitsu).
None of them have been particularily successful either in monetization or popularity - this is something I hope to change. <br/>
<br/>
My general idea is to something similar to the successful (in Japan) Idolmaster/Love Live mobile games:<br/>
Simple rhythm gameplay as the core, supported by RPG-ish progression.<br/>
My goal is to also have a ELO grind as the "core game". This is a model that's been shown to work (Clash of Clans, Hearthstone, etc) but hasn't really "happened" in Roblox yet.

